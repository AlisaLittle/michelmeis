export const DateFormatter = (date) => {
  const day = date.substring(8, 10);
  const month = date.substring(5, 7);
  const year = date.substring(0, 4);
  const nameOfMonth = (m) => {
    switch (m) {
      case "O1":
        return "Jan";
      case "02":
        return "Feb";
      case "03":
        return "Mar";
      case "04":
        return "Apr";
      case "05":
        return "May";
      case "06":
        return "Jun";
      case "07":
        return "Jul";
      case "08":
        return "Aug";
      case "09":
        return "Sep";
      case "10":
        return "Oct";
      case "11":
        return "Nov";
      case "12":
        return "Dec";
      default:
        console.log("Sorry, we are out of " + m + ".");
    }
  };
  const newDate = nameOfMonth(month) + " " + day + ". " + year;
  return newDate;
};
